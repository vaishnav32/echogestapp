import mongoose from "mongoose";

const controllerSchema = new mongoose.Schema({
  controllerId: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("Controller", controllerSchema);
